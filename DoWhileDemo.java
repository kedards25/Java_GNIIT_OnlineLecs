public class DoWhileDemo
{
	public static void main(String[] args)
	{
		int i=50;	//initial value
		do	
		{
			//System.out.println("Hello");
			System.out.println(i);
			i++;	//incr
		}while(i<=10);//condition
		//There is ';' after while because while does not have block 
		// belonging to it and it is terminating the whole block of iteration
	}
}